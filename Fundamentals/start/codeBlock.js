/*
  Bloco em js 
  Para  definir um bloco em js você ira usar um par de chaves {}, sendo um de abertura e outro de fechamneto 
  e dentro do par de chaves voçê pode iserir marias senteças de código que ficarão agrupadas dentro do par de chaves.
  
*/

//Exemplos de bloco de código:


// Com apenas uma sentença
{
    console.log('Bom dia!')
}

// Com mais de  uma sentença
{
    console.log('Bom dia!')
    console.log('Boa tarde!!')
    console.log('Boa noite!!!')
}

// Com dois bloco de codigo
{
    console.log('Bom dia!')
}

{
    console.log('Bom dia!')
    console.log('Boa tarde!!')
    console.log('Boa noite!!!')
}

// Com varios bloco de codigo

{
    {
        {
            {
                {
                    {
                        
                    }
                }
            }
        }
    }
}